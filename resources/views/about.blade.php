                  <li>Fomentar la integración y unidad. Se estimularán iniciativas que generen intercambio social y relaciones de amistad.</li>
                  <li>Impulsar y/o apoyar manifestaciones culturales.</li>
                  <li>Mantener un rol activo en la comunidad.</li>
