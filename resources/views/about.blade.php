                  <li>Fomentar diversas disciplinas deportivas y la sana competencia.</li>
                  <li>Impulsar un sentimiento de identidad y pertenencia al Club, y fundamentalmente a la comunidad de Bragado.</li>
                  <li>Promover el asociacionismo, la ayuda mutua, la participación activa y poder asumir compromisos y responsabilidades.</li>
                  <li>Favorecer el conocimiento y la utilización de los recursos de ocio que ofrece la comunidad.</li>
                  <li>Promoveremos un club que cuide el medio ambiente, desarrollaremos instalaciones deportivas ecológicas.</li>
